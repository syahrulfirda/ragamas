              <h2>Kebakaran di kalidam</h2>
              <p>
                Api melalap gudang tempat penyimpanan solar dan mes TNI di Jalan Kalidam, Kota Cimahi. Asap hitam membumbung tinggi dari lokasi kebakaran yang berada di kawasan permukiman TNI. Kobaran api dengan cepat membesar dan hampir menyambar SPBU milik TNI di sebelahnya.
                Peristiwa kebakaran itu berlangsung Senin (2/8/2021) pagi. Petugas pemadam kebakaran tiba di lokasi sekitar pukul 09.50 WIB kemudian bergegas melakukan upaya pemadaman. Api masih berkobar membakar bangunan gudang solar, mes anggota TNI, dapur, dan garasi rumah warga. Tidak ada korban jiwa dalam kejadian kebakaran ini."Laporan awal itu kebakaran SPBU milik TNI di Kalidam. Tapi setelah kami tiba di lokasi, ternyata objek yang terbakar itu gudang bahan bakar solar dan mes prajurit yang memang posisinya bersebelahan," kata Komandan Regu I Damkar Kota Cimahi Indrahadi.Berdasarkan keterangan saksi mata, menurut dia, api diduga berasal dari ledakan di gudang solar tersebut. Tak perlu menunggu lama, api dengan cepat membesar karena di dalam gudang tersebut ada bahan mudah terbakar.
                
                "Dugaan awal kebakaran menurut saksi mata itu awalnya ada ledakan di gudang drum solar. Mereka mengira itu bukan ledakan dari SPBU di sebelahnya. Tapi sadar setelah ada kepulan asap hitam pekat dan api sudah sangat besar saat mereka cek," kata Indra.
                
                Solar terbakar itu cepat memuai sehingga proses pemadaman tak memerlukan waktu lama dan tanpa perlu menggunakan cairan kimia untuk memadamkan kobaran api yang tersisa. "Kami terjunkan tiga unit pancar dan satu unit rescue. Kondisi di lokasi sangat merah, jadi kami butuh suplai air yang banyak. Kebetulan api sudah lama berkobar sehingga cadangan solar yang terbakar sudah menipis. Jadi kami tidak perlu penggunaan cairan khusus seperti AF3 dan cairan kimia lainnya untuk pemadaman," tutur Indra.
